    private void botonagregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonagregarActionPerformed
        // TODO add your handling code here:
        if (txtnombre.getText().isEmpty() || txtprecio.getText().isEmpty() || txtreceta.getText().isEmpty() || combotipo.getSelectedItem() == null || combosabor.getSelectedItem() == null){
            JOptionPane.showMessageDialog(null, "Ingrese todos los campos. Intente de nuevo","Informacion", JOptionPane.INFORMATION_MESSAGE);
        }
        else {
            if (!ExisteReceta()){
                String nombre = txtnombre.getText();
                String receta = txtreceta.getText();
                Float precio = Float.valueOf(txtprecio.getText());
                String sabor = combosabor.getSelectedItem().toString();
                String tipo = combotipo.getSelectedItem().toString();
                Registro p5 = new Registro (nombre, tipo, sabor, receta, precio);
                try {
                    SQLConnect objConnect = new SQLConnect();
                    SQLregistro objusuario = new SQLregistro(objConnect);
                    objusuario.InsertaRegistro(p5);

                    if (objConnect != null)
                    objConnect.close();
                    objConnect = null;
                    objusuario = null;
                } catch (SQLException ex) {
                 Logger.getLogger(Panelrecetas.class.getName()).log(Level.SEVERE, null, ex);
                }

            }
        }
    }//GEN-LAST:event_botonagregarActionPerformed

