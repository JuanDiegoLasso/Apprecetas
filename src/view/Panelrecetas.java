    private void FillTablerooms() {
        try {
            SQLConnect objConnect = new SQLConnect();
            SQLregistro objregistro = new SQLregistro(objConnect);

            List<Registro> registros = objregistro.ConsultaRecetas();


            CleanTableRecipes();

            for (Registro obj : registros) {

                    var model = (DefaultTableModel) tablarecetas.getModel();
                    model.addRow(new Object[]{
                         obj.getNombre(),obj.getTiporeceta(), obj.getSabor(),
                        obj.getPrecio(), obj.getRecetapap()
                    });
                }
            

            if (objConnect != null)
                objConnect.close();

            }catch (SQLException ex) {
            System.out.println("Exception Thrown: " + ex);
            // Logger.getLogger(PanelCheckin.class.getName()).log(Level.SEVERE, null, ex);
        }
}
